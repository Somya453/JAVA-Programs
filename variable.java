public class variable {


    public static void main(String[] args) {
        byte a = 15;
        byte b = 25;
        int sum = a + b;
        System.out.println("The sum is: " + sum);
        int age = 25; // integer variable
        if (age > 18) {
            System.out.println("You are an adult.");
        } else {
            System.out.println("You are not an adult.");
        }

        System.out.println("Hello, World!");
    }
}


// Data Types in Java:
// 1. Primitive Data Types:
//    - int: Integer data type (e.g., 1, 2, 3)
//    - float: Floating-point data type (e.g., 1.5)
//    - double: Double-precision floating-point data type (e.g., 3.14)
//    - char: Character data type (e.g., 'a', 'b', 'c')
//    - boolean: Boolean data type (true or false)
//    - byte: 8-bit signed integer data type
//    - short: 16-bit signed integer data type
//    - long: 64-bit signed integer data type

// 2. Non-Primitive Data Types:
//    - String: Sequence of characters (e.g., "Hello, World!")
//    - Arrays: Collection of similar data types (e.g., int[] arr = {1, 2, 3})
//    - Classes: User-defined data types that can contain variables and methods
//    - Interfaces: Abstract data types that define a contract for classes to implement
//    - Enums: Special data types that define a set of named constants
//    - Collections: Data structures that can store multiple objects (e.g., List, Set, Map)
//    - Wrapper Classes: Classes that encapsulate primitive data types (e.g., Integer, Double, Character)
//    - Date and Time: Classes for handling date and time (e.g., LocalDate, LocalTime, LocalDateTime)
//    - BigInteger and BigDecimal: Classes for handling large integers and high-precision decimal

